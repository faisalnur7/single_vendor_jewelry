<script>
    function slugify(text) {
        return text
            .toString()
            .toLowerCase()
            .trim()
            .replace(/[\s\W-]+/g, '-')
            .replace(/^-+|-+$/g, '');
    }

    $(document).ready(function() {
        var slugManuallyEdited = false;

        $('.slugify').on('input', function() {
            if (!slugManuallyEdited) {
                let slug = slugify($(this).val());
                $('.slug_text').val(slug);
            }
        });
    })
</script>
